# 7. Write a program that prints what action to take based on the color of a traffic light:
# •"Stop" for red
# •"Caution" for yellow•
# "Go" for green

light = "green"
if light == "red":
    print("Stop")
elif light == "yellow":
    print("Caution")
elif light == "green":
    print("Go")
else:
    print("Invalid input")
